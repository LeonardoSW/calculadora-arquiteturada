package calculadora.services;

public class CalculateHandle {
	
	public double CalculateSUM(double firstNumber, double secoundNumber) {
		return firstNumber + secoundNumber;
	}
	
	public double CalculateSUBTRACTION(double firstNumber, double secoundNumber) {
		return firstNumber - secoundNumber;
	}
	
	public double CalculateDIVISION(double firstNumber, double secoundNumber) {
		return firstNumber / secoundNumber;
	}
	
	public double CalculateMULTIPLICATION(double firstNumber, double secoundNumber) {
		return firstNumber * secoundNumber;
	}
	
}
